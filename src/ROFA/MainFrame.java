    private void addChairBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addChairBtnActionPerformed
        if(_furnitureManager.checkOrderCapacity()){
            
            int idNumber = Integer.parseInt(chairIdNumber.getText());
            
            if(_furnitureManager.checkIdNumberExists(idNumber)){
                JOptionPane.showMessageDialog(jPanel4, "Oops, ID in use.");
                return;
            }
                
            String woodType = chairWoodType.getSelectedItem().toString();
            int qty = Integer.parseInt(chairQty.getText());
            boolean armRests = chairArmRests.isSelected();
            
            Chair newChair = _furnitureManager.addNewChair(idNumber, woodType, qty, armRests);
            this.addGridImage(newChair.image, _furnitureManager.getOrderGridPosition(newChair.idNumber), newChair.idNumber);
            this.updateTotalPriceGUI();
            closeChairFormActionPerformed(evt);
            
        }
        else{
            JOptionPane.showMessageDialog(jPanel4, "Oops, you've reached the maximum number of items that can be added into cart.");
        }
    }//GEN-LAST:event_addChairBtnActionPerformed

