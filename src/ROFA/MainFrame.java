    private void gridClick(java.awt.event.MouseEvent evt, JLabel grid){
        int idNumber = "".equals(grid.getText()) ? 0 : Integer.parseInt(grid.getText());
        if(evt.getButton() == java.awt.event.MouseEvent.BUTTON1) {
            if(idNumber > 0) displayItemDetails(idNumber);
        }
        if(evt.getButton() == java.awt.event.MouseEvent.BUTTON2) {
            //
        }
        if(evt.getButton() == java.awt.event.MouseEvent.BUTTON3) {
            if(idNumber > 0) removeGridItem(grid);
        }
    }
    
    private void checkEmptyGrid(JLabel grid){
        if("".equals(grid.getText())){
            JOptionPane.showMessageDialog(jPanel4, "Oops, No item added to this grid yet.");
        }
    }
    
    private void displayItemDetails(int idNumber){
        _furnitureManager.displayItemOnConsole(idNumber);
        JOptionPane.showMessageDialog(jPanel4, "Details displayed to console.");
    }
    
    public void removeGridItem(JLabel grid){
        int idNumber = "".equals(grid.getText()) ? 0 : Integer.parseInt(grid.getText());
        
        if(_furnitureManager.checkIdNumberExists(idNumber)){
            int optionPane = JOptionPane.showConfirmDialog(jPanel4, "Please confirm action ?");
            if(optionPane == JOptionPane.YES_OPTION){
                this.addGridImage("", _furnitureManager.getOrderGridPosition(idNumber), 0);
                grid.setText("");
                _furnitureManager.removeGridItem(idNumber);
                this.updateTotalPriceGUI();
            }
        }
    }
    
